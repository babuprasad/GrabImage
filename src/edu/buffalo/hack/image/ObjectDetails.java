package edu.buffalo.hack.image;

public class ObjectDetails {
	
	String objectTitle;
	String objectImage;
	String objectUrl;
	String relevantUrl;
	
	public String getObjectTitle() {
		return objectTitle;
	}
	public String getObjectImage() {
		return objectImage;
	}
	public String getObjectUrl() {
		return objectUrl;
	}
	public String getRelevantUrl() {
		return relevantUrl;
	}
	
	
}